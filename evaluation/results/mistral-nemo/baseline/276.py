import folium

# Входные данные
N0 = 100  # Начальная популяция кроликов
r = 0.05  # Коэффициент роста (в день)
F = 20   # Количество корма на единицу времени (в кг)
k = 0.02  # Коэффициент конверсии корма в рост популяции

# Вычисление вероятности выживания и размножения
p = k * F

# Вычисление населения на каждый момент времени
t_max = 365  # Максимальное время моделирования (в днях)
N = [N0 * (1 + r * p)**t for t in range(t_max)]

# Создание карты и добавление маркеров для каждого момента времени
m = folium.Map(location=[48.7102, 2.2157], zoom_start=6)  # Примерные координаты Парижа

for i, n in enumerate(N):
    folium.Marker([48.7102 + i/1000, 2.2157]).add_to(m)

# Сохранение карты
m.save("276.html")