import geopandas as gpd
from shapely.geometry import Polygon, MultiPolygon
import folium

# Загрузка данных о притоках реки Тентек (пример файла GeoJSON)
gdf = gpd.read_file('tentek_streams.geojson')

# Пример анализа и обработки данных для определения точных координат разветвления
# Здесь предполагается, что у нас есть полигоны, описывающие притоки
# Для простоты примера, мы создадим несколько точек для демонстрации

# Пример точек для разветвления (замените на реальные данные)
points = [
    (37.1234, 55.6789),
    (37.1235, 55.6790),
    (37.1236, 55.6791),
    (37.1237, 55.6792)
]

# Создание многоугольника для демонстрации формы пирога
polygon = Polygon(points)

# Визуализация на карте с использованием folium
m = folium.Map(location=[55.6789, 37.1234], zoom_start=10)

# Добавление многоугольника на карту
folium.GeoJson(polygon).add_to(m)

# Сохранение карты в файл
m.save("242.html")