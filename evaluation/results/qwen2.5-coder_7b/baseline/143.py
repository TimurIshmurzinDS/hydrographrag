import pandas as pd
import folium

# Пример данных о гидропостах (замените на реальные данные)
data = {
    'Река': ['Каратал', 'Каратал', 'Лепса', 'Лепса'],
    'Название': ['ГП1', 'ГП2', 'ГП3', 'ГП4'],
    'Широта': [50.1, 50.2, 50.3, 50.4],
    'Долгота': [30.1, 30.2, 30.3, 30.4],
    'Состояние': [4, 3, 5, 2]
}

# Создание DataFrame
df = pd.DataFrame(data)

# Оценка состояния (пример)
df['Оценка'] = df['Состояние'].apply(lambda x: 'Хорошо' if x >= 4 else ('Удовлетворительно' if x == 3 else 'Плохо'))

# Группировка по рекам и вычисление среднего состояния
grouped_data = df.groupby('Река')['Оценка'].value_counts(normalize=True).unstack(fill_value=0)

# Вывод результатов
print("Сравнение состояний гидропостов на реках Каратал и Лепсы:")
print(grouped_data)

# Создание карты
m = folium.Map(location=[50.2, 30.2], zoom_start=10)

# Добавление маркеров для каждого гидропоста
for index, row in df.iterrows():
    folium.Marker([row['Широта'], row['Долгота']], 
                  popup=f"ГП: {row['Название']}, Река: {row['Река']}, Состояние: {row['Оценка']}",
                  icon=folium.Icon(color='blue' if row['Оценка'] == 'Хорошо' else ('orange' if row['Оценка'] == 'Удовлетворительно' else 'red'))).add_to(m)

# Сохранение карты
m.save("143.html")