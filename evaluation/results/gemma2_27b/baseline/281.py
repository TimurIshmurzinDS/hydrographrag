import folium

# Данные о точности методов (заменить на реальные данные)
data = {
    "Метод": ["Старый метод 1", "Современный алгоритм A"],
    "Точность (%)": [50, 90],
    "Координаты": [(40.7128, -74.0060), (34.0522, -118.2437)],
}

# Создание карты
m = folium.Map(location=[40, 0], zoom_start=2)

# Добавление маркеров с информацией о точности
for i in range(len(data["Метод"])):
    folium.Marker(
        location=data["Координаты"][i],
        popup=f"Метод: {data['Метод'][i]}<br>Точность: {data['Точность'][i]}%",
    ).add_to(m)

# Сохранение карты
m.save("281.html")