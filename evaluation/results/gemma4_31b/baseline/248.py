import folium

def generate_mars_essay():
    essay = """
    Эссе: 'Проект Аксу: От земных степей к красным пескам Марса'
    
    Колонизация Марса требует не просто технологий, а глубокого понимания управления ресурсами в экстремальных условиях. 
    В качестве архитектурного и логистического шаблона для первой марсианской базы мы выбрали геометрию реки Аксу. 
    
    Река Аксу, прорезающая засушливые ландшафты, является идеальным примером 'линии жизни'. В нашем моделировании 
    координаты реки трансформируются в 'Магистраль Жизнеобеспечения' (Life-Support Spine). 
    
    1. Истоки (Верховья): В точке начала реки на Марсе будет расположен главный ледовый комбинат по добыче воды из 
       подповерхностных слоев. 
    2. Меандры (Изгибы): Каждый изгиб реки Аксу в нашей модели становится зоной затишья, где будут размещены 
       купольные жилые модули, защищенные от марсианских пылевых бурь.
    3. Притоки: Точки слияния малых рек с Аксу станут узлами интеграции — здесь будут расположены научно-исследовательские 
       центры и станции связи.
    4. Дельта: Нижнее течение реки превращается в обширную зону гидропонных ферм, где вода, прошедшая через все 
       фильтры колонии, будет использоваться для выращивания пищи.
    
    Таким образом, используя географический код реки Аксу, мы переносим земной опыт ирригации и выживания в 
    аридных зонах на межпланетный уровень, превращая земную гидрографию в карту будущего человечества.
    """
    return essay

def create_mars_map():
    # Приблизительные координаты реки Аксу (фрагмент для визуализации)
    # В реальности здесь может быть полноценный GeoJSON файл
    aksu_coords = [
        [42.5, 81.5], [42.3, 81.8], [42.1, 82.1], 
        [41.8, 82.5], [41.5, 82.8], [41.2, 83.2], 
        [40.8, 83.5], [40.5, 83.8]
    ]
    
    # Создаем карту, центрированную на регионе
    m = folium.Map(location=[41.5, 82.5], zoom_start=6, tiles="Stamen Terrain")
    
    # Добавляем линию "Магистрали Жизнеобеспечения" (Река Аксу)
    folium.PolyLine(
        locations=aksu_coords, 
        color="red", 
        weight=5, 
        opacity=0.8, 
        tooltip="Mars Life-Support Spine (Aksu Template)"
    ).add_to(m)
    
    # Расставляем маркеры колонии на основе координат
    colony_nodes = {
        aksu_coords[0]: "Центр добычи льда (Исток)",
        aksu_coords[3]: "Жилой сектор 'Оазис' (Меандр)",
        aksu_coords[5]: "Научный хаб 'Синтез' (Узел)",
        aksu_coords[-1]: "Гидропонные фермы (Дельта)"
    }
    
    for coord, name in colony_nodes.items():
        folium.Marker(
            location=coord, 
            popup=name, 
            icon=folium.Icon(color="darkred", icon="info-sign")
        ).add_to(m)
    
    # Сохранение карты
    m.save("248.html")

if __name__ == "__main__":
    # 1. Генерация эссе
    mars_essay = generate_mars_essay()
    print(mars_essay)
    
    # 2. Создание карты
    create_mars_map()
    print("\n[Система]: Карта-схема колонизации сохранена в файл 248.html")