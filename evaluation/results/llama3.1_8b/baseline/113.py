import pandas as pd
from folium import Map, Marker
from folium.plugins import HeatMap

# Подготовка данных
data = {
    'Река': ['Лепса', 'Сарыкан'],
    'Уровень воды (м)': [10, 15],
    'Эндемичные виды флоры и фауны': ['Вид1', 'Вид2']
}

df = pd.DataFrame(data)

# Создание модели
def оценка_экологических_последствий(река, уровень_воды):
    if река == 'Лепса' and уровень_воды < 10:
        return 'Пониженный уровень воды может привести к вымиранию эндемичных видов флоры и фауны.'
    elif река == 'Сарыкан' and уровень_воды < 15:
        return 'Пониженный уровень воды может привести к изменению ареала обитания эндемичных видов флоры и фауны.'

# Анализ данных и оценка последствий
for index, row in df.iterrows():
    последствия = оценка_экологических_последствий(row['Река'], row['Уровень воды (м)'])
    print(f'Для реки {row["Река"]} потенциальные экологические последствия снижения уровня воды: {последствия}')

# Визуализация на карте
m = Map(location=[55.0, 60.0], zoom_start=6)
for index, row in df.iterrows():
    Marker([row['lat'], row['lon']], popup=row['Река']).add_to(m)

HeatMap(df[['lat', 'lon']].values, radius=10).add_to(m)

m.save("113.html")