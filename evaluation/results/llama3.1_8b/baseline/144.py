import pandas as pd
from folium import Map, Marker, Icon
import geopandas as gpd

# Загрузить данные о бассейне реки Или и расположении датчиков уровня воды.
bassейн_река = gpd.read_file('bassейн_река.shp')
датчики_уровня_воды = pd.read_csv('датчики_уровня_воды.csv')

# Проанализировать данные о текущем уровне воды в каждом из датчиков.
датчики_уровня_воды['норма'] = 10  # примерное значение нормы уровня воды
датчики_уровня_воды['оповещение'] = датчики_уровня_воды.apply(lambda x: 'да' if x['уровень'] > x['норма'] else 'нет', axis=1)

# Определить, есть ли какие-либо оповещения от датчиков с превышением нормы уровня воды.
оповещения = датчики_уровня_воды[датчики_уровня_воды['оповещение'] == 'да']

# Визуализировать результат на карте.
m = Map(location=[55.7558, 37.6173], zoom_start=10)
for index, row in оповещения.iterrows():
    Marker(
        location=[row['широта'], row['долгота']],
        icon=Icon(color='red'),
        popup=f'Датчик {index} с превышением нормы уровня воды'
    ).add_to(m)

m.save("144.html")