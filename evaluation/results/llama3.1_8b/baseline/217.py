import folium
import pandas as pd

# Сбор данных о температуре и влажности воздуха из датчиков Кызылординской области
data = {
    'район': ['Кызылорда', 'Туркестан', 'Сырдарья', 'Нуротау'],
    'температура': [20, 22, 18, 25],
    'влажность': [50, 55, 45, 60]
}

df = pd.DataFrame(data)

# Анализ данных для определения оптимальных условий для выращивания кактусов
optimal_conditions = df[(df['температура'] >= 18) & (df['температура'] <= 25) & (df['влажность'] >= 40) & (df['влажность'] <= 60)]

# Создание карты с указанием районов, где условия подходят для выращивания кактусов
m = folium.Map(location=[43.65, 69.33], zoom_start=6)

for index, row in optimal_conditions.iterrows():
    folium.Marker([row['район'].latitude, row['район'].longitude],
                  popup=f'Район: {row["район"]}\n'
                        f'Температура: {row["температура"]}°C\n'
                        f'Влажность: {row["влажность"]}%',
                  icon=folium.Icon(color='green')).add_to(m)

# Добавление подсказок и рекомендаций на карте
folium.Marker([43.65, 69.33],
              popup=f'Для выращивания кактусов необходима температура от 18 до 25°C и влажность от 40 до 60%.',
              icon=folium.Icon(color='blue')).add_to(m)

# Сохранение карты в файл
m.save("217.html")