import pandas as pd
from folium import Map, Marker

# Загрузите данные о уровнях воды для реки Кіші Алматы
data = {
    'Дата': ['2022-01-01', '2022-02-01', '2022-03-01', '2022-04-01'],
    'Уровень воды (м)': [10, 15, 20, 25]
}
df = pd.DataFrame(data)

# Определите пиковый уровень воды в данных
peak_water_level = df['Уровень воды (м)'].max()

# Вычислите средний многолетний показатель уровня воды
average_water_level = df['Уровень воды (м)'].mean()

# Сравните пиковый и средний уровни воды
print(f'Пиковый уровень воды: {peak_water_level} м')
print(f'Средний многолетний показатель уровня воды: {average_water_level} м')

# Создайте карту с маркером для пикового уровня воды
m = Map(location=[43.25, 76.95], zoom_start=10)
Marker([43.25, 76.95], popup=f'Пиковый уровень воды: {peak_water_level} м').add_to(m)

# Сохраните карту в файл
m.save("122.html")