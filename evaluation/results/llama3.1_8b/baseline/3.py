import pandas as pd
import folium

# Шаг 1: Скачать данные о уровне воды на реке Талгар.
# Для демонстрации мы будем использовать фиктивные данные, которые можно заменить на реальные данные в зависимости от источника.

data = {
    'Дата': ['2022-01-01', '2022-01-02', '2022-01-03'],
    'Река': ['Талгар'],
    'Уровень воды (м)': [10.5, 11.2, 9.8]
}

df = pd.DataFrame(data)

# Шаг 2: Прочитать данные в формате CSV или другой подходящей для данных геоинформационной системы (GIS) структуры.
# Для демонстрации мы будем использовать DataFrame из pandas.

# Шаг 3: Извлечь последний показатель уровня воды из данных.
last_water_level = df.loc[df['Дата'].idxmax()]['Уровень воды (м)']

print(f"Последний показатель уровня воды на реке Талгар: {last_water_level} м")

# Шаг 4: Если требуется визуализация на карте, использовать библиотеку Folium для создания интерактивной карты с указанием последнего показателя уровня воды.
m = folium.Map(location=[43.0, 76.5], zoom_start=10)

folium.Marker([43.0, 76.5], popup=f"Последний уровень воды: {last_water_level} м").add_to(m)

# Сохранить карту в файле HTML.
m.save("3.html")