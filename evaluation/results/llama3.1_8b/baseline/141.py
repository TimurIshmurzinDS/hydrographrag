import pandas as pd
import folium

# Загрузите данные о уровнях воды из источника мониторинга (в этом примере мы используем фиктивные данные)
data = {
    'Река': ['Дон', 'Волга', 'Нева', 'Обь'],
    'Уровень воды': [10, 15, 8, 12],
    'Критический уровень': [9, 14, 7, 11]
}
df = pd.DataFrame(data)

# Определите критический уровень воды для каждой реки
critical_rivers = df[df['Уровень воды'] > df['Критический уровень']]['Река'].tolist()

# Создайте карту с помощью библиотеки Folium
m = folium.Map(location=[55.7558, 37.6173], zoom_start=5)

# Добавьте маркеры на реках с критическим уровнем воды
for river in critical_rivers:
    folium.Marker([55.7558, 37.6173], popup=f'Река: {river}, Критический уровень воды').add_to(m)

# Сохраните карту в файл
m.save("141.html")