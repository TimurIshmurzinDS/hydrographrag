import pandas as pd
import numpy as np
from folium import Map, Marker

# Загрузим данные о показаниях датчиков уровня воды для рек Или и Каратал.
data = {
    'Дата': ['2022-01-01', '2022-01-02', '2022-01-03', '2022-01-04', '2022-01-05'],
    'Река': ['Или', 'Каратал', 'Или', 'Каратал', 'Или'],
    'Уровень воды (м)': [10.5, 12.8, 11.2, 13.1, 10.9]
}

df = pd.DataFrame(data)

# Рассчитаем среднее значение показаний для каждого дня.
grouped_df = df.groupby('Дата')['Уровень воды (м)'].mean().reset_index()

# Рассчитаем коэффициент вариации (CV) для каждой группы данных.
grouped_df['CV'] = grouped_df['Уровень воды (м)'] / grouped_df.groupby('Дата')['Уровень воды (м)'].transform(np.std)

# Создадим интерактивную карту с маркерами для каждого датчика уровня воды.
m = Map(location=[43.0, 77.0], zoom_start=6)
for index, row in df.iterrows():
    if row['Река'] == 'Или':
        Marker([row['Дата'], row['Уровень воды (м)']], popup=f'Или: {row["Уровень воды (м)"]} м').add_to(m)
    else:
        Marker([row['Дата'], row['Уровень воды (м)']], popup=f'Каратал: {row["Уровень воды (м)"]} м').add_to(m)

# Сохраним карту как HTML-файл.
m.save("150.html")