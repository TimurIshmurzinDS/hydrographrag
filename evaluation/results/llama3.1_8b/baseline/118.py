import pandas as pd
from folium import Map, Marker, Popup

# Данные о реках (предположительно полученные из другого источника)
data = {
    'Река': ['Сарыкан', 'Темирлик'],
    'Длина': [100, 120],
    'Ширина': [5, 6],
    'Скорость течения': [1.2, 1.5]
}

# Создаем датафрейм
df = pd.DataFrame(data)

# Рассчитываем площадь водосбора для каждой реки
df['Площадь'] = df['Длина'] * df['Ширина']

# Рассчитываем объем воды в каждой реке
df['Объем воды'] = df['Площадь'] * df['Скорость течения']

# Создаем карту
m = Map(location=[40.0, 70.0], zoom_start=6)

# Добавляем маркеры на карту для каждой реки
for index, row in df.iterrows():
    marker = Marker(
        location=[row['Длина'] / 2, row['Ширина'] / 2],
        popup=f'Река: {row["Река"]}<br>Объем воды: {row["Объем воды"]} куб.м'
    )
    m.add_child(marker)

# Сохраняем карту в файл
m.save("118.html")