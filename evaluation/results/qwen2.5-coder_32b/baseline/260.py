import folium

# Координаты примерных точек сбора ила из реки Караой
sampling_points = [
    (42.8506, 74.5913),  # Примерная координата 1
    (42.8520, 74.5930),  # Примерная координата 2
    (42.8535, 74.5945)   # Примерная координата 3
]

# Создание карты с центром в районе реки Караой
m = folium.Map(location=[42.8516, 74.5926], zoom_start=14)

# Добавление маркеров для точек сбора образцов
for point in sampling_points:
    folium.Marker(point, popup='Точка сбора ила').add_to(m)

# Сохранение карты в файл
m.save("260.html")