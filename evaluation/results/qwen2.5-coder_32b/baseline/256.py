import folium

# Координаты реки Талгар (примерные)
lat, lon = 42.853611, 74.590556

# Создание карты с центром в месте реки Талгар
m = folium.Map(location=[lat, lon], zoom_start=12)

# Добавление маркера на место реки Талгар
folium.Marker(
    [lat, lon],
    popup='Река Талгар',
    tooltip='Нажмите для информации'
).add_to(m)

# Сохранение карты в файл 256.html
m.save("256.html")

