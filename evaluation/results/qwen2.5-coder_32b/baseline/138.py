import pandas as pd
import folium

# Шаг 1: Сбор данных (примерные данные для демонстрации)
data = {
    'Река': ['Лепса', 'Бутак'],
    'Уровень воды, м': [2.5, 3.0],
    'Широта': [46.9178, 46.9200],  # Примерные координаты
    'Долгота': [31.9990, 32.0050]   # Примерные координаты
}

# Шаг 2: Обработка данных
df = pd.DataFrame(data)

# Шаг 3: Анализ данных (просто выводим DataFrame для демонстрации)
print(df)

# Шаг 4: Визуализация данных на карте с использованием folium
m = folium.Map(location=[46.9189, 32.0020], zoom_start=13)  # Центральная точка между реками

for index, row in df.iterrows():
    folium.Marker(
        location=[row['Широта'], row['Долгота']],
        popup=f"Река: {row['Река']}, Уровень воды: {row['Уровень воды, м']} м",
        icon=folium.Icon(color='blue')
    ).add_to(m)

# Шаг 5: Сохранение карты в файл HTML
m.save("138.html")