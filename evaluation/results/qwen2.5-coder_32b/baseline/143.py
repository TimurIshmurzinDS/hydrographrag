import pandas as pd
import folium

# Шаг 1: Сбор данных (примерные данные)
data = {
    'Река': ['Каратал', 'Каратал', 'Лепса', 'Лепса'],
    'Название гидропоста': ['ГП1', 'ГП2', 'ГП3', 'ГП4'],
    'Широта': [50.1, 50.2, 49.8, 49.7],
    'Долгота': [60.1, 60.2, 59.8, 59.7],
    'Состояние': ['Работает', 'Не работает', 'Работает', 'Не работает']
}

# Шаг 2: Подготовка данных
df = pd.DataFrame(data)

# Шаг 3: Анализ данных (просто выводим таблицу для наглядности)
print(df)

# Шаг 4: Визуализация результатов с помощью folium
m = folium.Map(location=[50.0, 60.0], zoom_start=10)

# Функция для определения цвета маркера в зависимости от состояния гидропоста
def get_color(state):
    return 'green' if state == 'Работает' else 'red'

# Добавление маркеров на карту
for index, row in df.iterrows():
    folium.Marker(
        location=[row['Широта'], row['Долгота']],
        popup=f"{row['Название гидропоста']} ({row['Состояние']})",
        icon=folium.Icon(color=get_color(row['Состояние']))
    ).add_to(m)

# Сохранение карты в файл
m.save("143.html")