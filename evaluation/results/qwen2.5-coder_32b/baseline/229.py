import folium
import pandas as pd
from branca.colormap import LinearColormap

# Предположим, что у нас есть CSV-файл с данными о риске наводнений для реки Аксу.
# Файл содержит столбцы: 'latitude', 'longitude', 'flood_risk' (значение от 0 до 1).

# Шаг 1: Сбор данных
data = pd.read_csv('flood_risk_aksu.csv')

# Шаг 2: Обработка данных
# Предположим, что данные уже очищены и готовы к использованию.

# Шаг 3: Создание модели
# Для простоты предположим, что модель просто использует значение риска наводнения.
# В реальном случае можно было бы использовать более сложную модель машинного обучения.

# Шаг 4: Визуализация результатов
m = folium.Map(location=[42.8746, 74.5910], zoom_start=10)  # Координаты Бишкека

# Создание цветовой карты для отображения уровня риска наводнений
colormap = LinearColormap(
    colors=['green', 'yellow', 'orange', 'red'],
    vmin=data['flood_risk'].min(),
    vmax=data['flood_risk'].max()
)

# Добавление цветовой карты на карту
m.add_child(colormap)

# Добавление точек с риском наводнений на карту
for _, row in data.iterrows():
    folium.CircleMarker(
        location=[row['latitude'], row['longitude']],
        radius=5,
        color=colormap(row['flood_risk']),
        fill=True,
        fill_color=colormap(row['flood_rиск']),
        fill_opacity=0.7
    ).add_to(m)

# Шаг 5: Сохранение карты
m.save("229.html")

