import folium

# Шаг 1: Анализ условий Луны (в данном случае, мы предполагаем, что место выбрано)
# Шаг 2: Моделирование системы оросения (предположим, система адаптирована)
# Шаг 3: Выбор культур для выращивания (предположим, выбранные растения)

# Шаг 4: Создание цифровой модели фермы
# Предположим, что мы знаем координаты центра фермы на Луне
center_coordinates = [0.0, 0.0]  # Координаты примерно в центре Луны

# Создание карты с использованием folium
m = folium.Map(location=center_coordinates, zoom_start=4, tiles='Stamen Terrain')

# Добавление маркера для фермы
folium.Marker(
    location=center_coordinates,
    popup="Ферма на Луне",
    icon=folium.Icon(color='green')
).add_to(m)

# Шаг 5: Визуализация модели на карте
m.save("269.html")

print("Карта сохранена как 269.html")