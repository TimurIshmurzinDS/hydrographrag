import folium

# Координаты реки Шарын и точки измерения уровня паводка (примерные координаты)
sharyn_coords = [51.2836, 74.0922]  # примерная средняя точка реки Шарын
gauge_coords = [51.2836, 74.0922]   # предположим, что измерение уровня паводка происходит в этой точке

# Создание карты с центром в средней точке реки Шарын
m = folium.Map(location=sharyn_coords, zoom_start=13)

# Добавление маркера для реки Шарын
folium.Marker(
    location=sharyn_coords,
    popup='Река Шарын',
    icon=folium.Icon(icon='info-sign')
).add_to(m)

# Добавление маркера для точки измерения уровня паводка
folium.Marker(
    location=gauge_coords,
    popup='Точка измерения уровня паводка',
    icon=folium.Icon(color='red', icon='tint')
).add_to(m)

# Сохранение карты в файл
m.save("226.html")

# Пример использования данных о уровне воды для выбора количества яблок
water_level = 1.5  # примерный уровень паводка в метрах

if water_level > 1.0:
    apples_count = 8  # если уровень воды высокий, используем больше яблок
else:
    apples_count = 6  # иначе используем меньше яблок

# Вывод информации о количестве яблок для рецепта
print(f"Для приготовления яблочного пирога необходимо использовать {apples_count} яблок.")