import folium

# Создание карты с центром на приблизительном расположении реки Дос (координаты могут быть неточными)
m = folium.Map(location=[51.8309, 47.2683], zoom_start=10)

# Гипотетические данные о топографии реки Дос
# В реальности эти данные должны быть получены из геодатасета
coordinates = [
    [51.8309, 47.2683],
    [51.8310, 47.2684],
    [51.8311, 47.2685],
    [51.8312, 47.2686]
]

# Добавление линии на карту для отображения русла реки
folium.PolyLine(coordinates, color="blue", weight=2.5, opacity=1).add_to(m)

# Сохранение карты в файл
m.save("244.html")

