import folium
import random

# Шаг 1: Получение данных о уровне воды реки Узун Каргалы (используем случайные данные)
level_of_water = random.uniform(0.5, 3.0)  # Уровень воды в метрах

# Шаг 2: Преобразование уровня воды в условное значение для рецепта
# Предположим, что уровень воды влияет на количество воды в тесте пирога
water_quantity = level_of_water * 100  # Пример преобразования

# Шаг 3: Создание рецепта пирога с использованием полученного значения уровня воды
recipe = f"""
Рецепт пирога с уровнем воды реки Узун Каргалы:

Ингредиенты:
- Мука: 500 г
- Вода из реки Узун Каргалы: {water_quantity} мл
- Сахар: 100 г
- Яйца: 3 шт.
- Разрыхлитель: 1 ч.л.
- Творог: 200 г

Приготовление:
1. Замесить тесто из муки, воды, сахара и разрыхлителя.
2. Выкатать тесто и выложить его в форму для выпечки.
3. Смешать творог с сахаром и распределить по основанию теста.
4. Выпекать пирог при 180°C примерно 30 минут до золотистого цвета.

Приятного аппетита!
"""

print(recipe)

# Шаг 4: Визуализация данных на карте с помощью библиотеки folium
# Координаты реки Узун Каргалы (примерные)
uzyn_kargaly_coords = [42.853611, 79.033333]

m = folium.Map(location=uzyn_kargaly_coords, zoom_start=12)

# Добавление маркера с информацией о уровне воды
folium.Marker(
    location=uzyn_kargaly_coords,
    popup=f"Уровень воды: {level_of_water} м",
    icon=folium.Icon(color='blue')
).add_to(m)

# Шаг 5: Сохранение карты в файл 211.html
m.save("211.html")