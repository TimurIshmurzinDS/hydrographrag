import folium

# Данные о реке Караяй (примерные)
river_length_km = 1050  # Длина реки Караяй в километрах
river_length_m = river_length_km * 1000  # Переводим длину реки в метры

# Физические константы
G = 6.67430e-11  # Гравитационная постоянная, м³·кг⁻¹·с⁻²
c = 299792458    # Скорость света в вакууме, м/с

# Вычисление массы черной дыры
R = river_length_m  # Предполагаем, что длина реки равна радиусу горизонта событий
M = (R * c**2) / (2 * G)

print(f"Масса черной дыры: {M} кг")

# Координаты реки Караяй (примерные)
lat, lon = 43.0589, 76.8145

# Создание карты с помощью folium
m = folium.Map(location=[lat, lon], zoom_start=12)

# Добавление маркера на реку Караяй
folium.Marker([lat, lon], popup="Река Караяй").add_to(m)

# Сохранение карты в файл
m.save("245.html")