import geopandas as gpd
from shapely.geometry import Point, Polygon
import folium

# Шаг 1: Сбор данных
# Предположим, что у нас есть GeoDataFrame с данными о реке Шарын и ее сезонных разливах.
# Для примера создадим искусственные данные.

# Координаты точек, образующих полигон сезонного разлива реки Шарын
coords = [
    (76.9500, 43.2100),
    (76.9600, 43.2100),
    (76.9600, 43.2200),
    (76.9500, 43.2200)
]

# Создание полигона
polygon = Polygon(coords)

# Создание GeoDataFrame
gdf = gpd.GeoDataFrame(index=[0], crs="EPSG:4326", geometry=[polygon])

# Шаг 2: Обработка данных (в данном случае данные уже в нужном формате)
# Шаг 3: Анализ данных (для примера просто используем созданный полигон)

# Шаг 4: Визуализация
# Создание карты с использованием folium
m = folium.Map(location=[43.2150, 76.9550], zoom_start=13)

# Добавление полигона на карту
folium.GeoJson(gdf).add_to(m)

# Шаг 5: Сохранение результата
m.save("234.html")

print("Карта сохранена как 234.html")