import geopandas as gpd
import pandas as pd
import folium
from shapely.geometry import Point

# Шаг 1: Сбор данных
# Предположим, что у нас есть следующие данные в формате GeoJSON и CSV:
# - Водные ресурсы (ресурсы_воды.geojson)
# - Сельскохозяйственные участки (участки.geojson)
# - Климатические данные (климат.csv)

# Загрузка данных
водные_ресурсы = gpd.read_file('ресурсы_воды.geojson')
участки = gpd.read_file('участки.geojson')
климат = pd.read_csv('климат.csv')

# Шаг 2: Обработка данных
# Предположим, что данные уже предварительно обработаны и готовы к использованию

# Шаг 3: Моделирование водных ресурсов
# Простая модель водного баланса (пример):
водные_ресурсы['Объем_воды'] = водные_ресурсы['Площадь'] * климат['Средний_окапитальный_объем']

# Шаг 4: Оценка потребностей
участки['Требуемый_объем_воды'] = участки['Площадь'] * 0.1  # Примерная оценка потребности в воде

# Шаг 5: Анализ рисков дефицита
# Определяем зоны риска дефицита воды
участки = gpd.sjoin(участки, водные_ресурсы[['geometry', 'Объем_воды']], how="left", op='intersects')
участки['Риск_дефицита'] = участки['Требуемый_объем_воды'] > участки['Объем_воды']

# Шаг 6: Визуализация результатов
m = folium.Map(location=[51.2839, 47.6184], zoom_start=10)  # Координаты примерного центра бассейна реки Уржар

# Добавление участков с риском дефицита воды
for _, row in участки.iterrows():
    if row['Риск_дефицита']:
        folium.CircleMarker(
            location=[row.geometry.y, row.geometry.x],
            radius=5,
            color='red',
            fill=True,
            fill_color='red'
        ).add_to(m)

# Добавление водных ресурсов
водные_ресурсы.explore(m=m, column="Объем_воды", cmap="Blues")

# Сохранение карты в файл
m.save("182.html")