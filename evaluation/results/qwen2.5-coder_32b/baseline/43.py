import pandas as pd
import folium

# Шаг 1: Сбор данных
# Предположим, у нас есть данные о расходах воды в реке Аягоз и зонах оросения.
# Для примера создадим фиктивные данные.

# Данные о расходах воды (куб. м/с)
data = {
    'Дата': pd.date_range(start='2023-01-01', periods=12, freq='M'),
    'Расход_воды': [50, 60, 70, 80, 90, 100, 110, 120, 130, 140, 150, 160]
}

# Данные о зонах оросения (координаты и площадь в гектарах)
irrigation_zones = {
    'Название': ['Зона1', 'Зона2'],
    'Широта': [43.25, 43.30],
    'Долгота': [76.90, 76.85],
    'Площадь_га': [100, 150]
}

# Шаг 2: Обработка данных
df = pd.DataFrame(data)
zones_df = pd.DataFrame(irrigation_zones)

# Норма потребления воды для оросения (куб. м/га/сутки)
water_consumption_rate = 8

# Расчет общего объема воды, необходимого для оросения за месяц
monthly_water_needed = zones_df['Площадь_га'] * water_consumption_rate * 30  # Предполагаем 30 дней в месяце

# Шаг 3: Моделирование
# Простая модель: предположим, что весь расход воды используется для оросения.
df['Объем_воды_на_оросение'] = df['Расход_воды'] * 24 * 30  # Расход воды в куб. м/с * секунды в часе * часы в месяце

# Шаг 4: Визуализация
m = folium.Map(location=[43.275, 76.875], zoom_start=10)

for _, row in zones_df.iterrows():
    folium.Marker(
        location=[row['Широта'], row['Долгота']],
        popup=f"{row['Название']}: Площадь {row['Площадь_га']} га",
        icon=folium.Icon(color='blue')
    ).add_to(m)

# Добавление информации о расходах воды
for _, row in df.iterrows():
    folium.Marker(
        location=[43.275, 76.875],
        popup=f"Дата: {row['Дата'].strftime('%Y-%m')}, Расход воды: {row['Расход_воды']} куб.м/с",
        icon=folium.Icon(color='red')
    ).add_to(m)

# Сохранение карты
m.save("43.html")

# Вывод результата
print(df[['Дата', 'Объем_воды_на_оросение']])