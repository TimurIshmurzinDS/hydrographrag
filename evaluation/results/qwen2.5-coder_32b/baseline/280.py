import pandas as pd
import folium

# Шаг 1: Сбор данных
# Предположим, что у нас есть CSV файл с данными о средней стоимости проживания в крупных городах.
data = {
    'Город': ['Москва', 'Санкт-Петербург', 'Новосибирск', 'Екатеринбург'],
    '1900е': [25, 20, 15, 10],
    '1950е': [50, 40, 30, 20],
    '2000е': [100, 80, 60, 40],
    '2020е': [200, 160, 120, 80]
}

df = pd.DataFrame(data)

# Шаг 2: Обработка данных
# Предположим, что данные уже очищены и готовы к анализу.

# Шаг 3: Анализ данных
# Для простоты анализа рассчитаем среднее значение стоимости проживания за все периоды для каждого города.
df['Средняя стоимость'] = df[['1900е', '1950е', '2000е', '2020е']].mean(axis=1)

# Шаг 4: Визуализация результатов
# Координаты крупных городов для визуализации на карте.
coordinates = {
    'Москва': [55.7558, 37.6173],
    'Санкт-Петербург': [59.9386, 30.3141],
    'Новосибирск': [55.0302, 82.9236],
    'Екатеринбург': [56.8389, 60.6057]
}

# Создание карты
m = folium.Map(location=[55.7558, 37.6173], zoom_start=4)

# Добавление маркеров на карту с информацией о средней стоимости проживания
for index, row in df.iterrows():
    city = row['Город']
    avg_cost = row['Средняя стоимость']
    lat, lon = coordinates[city]
    
    folium.Marker(
        location=[lat, lon],
        popup=f"{city}: Средняя стоимость проживания - {avg_cost} руб.",
        icon=folium.Icon(color='blue')
    ).add_to(m)

# Сохранение карты в HTML файл
m.save("280.html")